const locales = { en, ru };

export function getLocale(code) {
  return locales[code] || locales['en'];
}

export function getCurrentLocale() {
  try {
    return localStorage.getItem('locale') || 'en';
  } catch (_) {
    return 'en';
  }
}

export function saveLocale(code) {
  try {
    localStorage.setItem('locale', code);
  } catch (_) {}
}
