const BK7_K_TABLE = BK7_TI_10MM.map(([lamNm, ti]) => {
    const d_mm = 10;
    const alpha_per_mm = -Math.log(Math.min(Math.max(ti, 1e-6), 1)) / d_mm;
    const lam_mm = lamNm / 1e6;                 // nm → mm
    return [lamNm, alpha_per_mm * lam_mm / (4 * Math.PI)];
});
function bk7ExtinctionK(lambda_nm) {
    const d = BK7_K_TABLE;
    if (lambda_nm <= d[0][0]) return d[0][1];
    if (lambda_nm >= d[d.length - 1][0]) return d[d.length - 1][1];
    let lo = 0, hi = d.length - 1;
    while (hi - lo > 1) { const m = (lo + hi) >> 1; if (d[m][0] <= lambda_nm) lo = m; else hi = m; }
    const t = (lambda_nm - d[lo][0]) / (d[hi][0] - d[lo][0]);
    return d[lo][1] + t * (d[hi][1] - d[lo][1]);
}
