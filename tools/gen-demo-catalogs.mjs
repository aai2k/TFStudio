// tools/gen-demo-catalogs.mjs — emit web/demo-catalogs.js for the web demo.
//
// The demo can't ship the built-in catalog's live getNK functions (not JSON-able),
// and — more importantly — the full 16-material built-in catalog makes the synthesis
// material POOL unwieldy out of the box. So we generate a few small, curated
// TWO-MATERIAL catalogs (classic high/low coating pairs) as TABULATED materials
// (formulaNum -1, tabData=[[λnm,n,k],…]) which catalogManager.makeGetNK rebuilds
// in the browser via interpolation. window.DEMO_CATALOGS is consumed by the shim's
// loadCatalogs() (see web/demo-shim.js).
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { getMaterial } from '../src/utils/materials/materialDatabase.js';

const here = path.dirname(fileURLToPath(import.meta.url));
const out = path.join(here, '..', 'web', 'demo-catalogs.js');

// Curated high/low coating pairs (id = built-in material id).
const PAIRS = [
  { id: 'pair_tio2_sio2',  name: 'AR/HR pair — TiO₂ / SiO₂',   members: ['TiO2', 'SiO2'] },
  { id: 'pair_ta2o5_sio2', name: 'AR/HR pair — Ta₂O₅ / SiO₂',  members: ['Ta2O5', 'SiO2'] },
  { id: 'pair_nb2o5_sio2', name: 'AR/HR pair — Nb₂O₅ / SiO₂',  members: ['Nb2O5', 'SiO2'] },
  { id: 'pair_tio2_mgf2',  name: 'AR pair — TiO₂ / MgF₂',      members: ['TiO2', 'MgF2'] },
];

// Sample grid (nm). Broad enough for QWOT sizing + visible/NIR plots; clamped to
// each material's real validity range so we never extrapolate the source data.
const GRID_MIN = 250, GRID_MAX = 1100, STEP = 5;

function sampleMaterial(id) {
  const m = getMaterial(id);
  const r = (typeof m.getNK === 'function' && m.getNK.rangeNm) || [GRID_MIN, GRID_MAX];
  const lo = Math.max(GRID_MIN, Math.ceil(r[0]));
  const hi = Math.min(GRID_MAX, Math.floor(r[1]));
  const tabData = [];
  for (let lam = lo; lam <= hi + 1e-9; lam += STEP) {
    const [n, k] = m.getNK(lam);
    tabData.push([lam, Math.round(n * 1e5) / 1e5, Math.round((k || 0) * 1e6) / 1e6]);
  }
  return {
    id: m.id,
    name: m.name,
    formulaNum: -1,
    coefficients: [],
    tabData,
    lambdaMin: lo / 1000,
    lambdaMax: hi / 1000,
    kTable: [],
    nd: null, vd: null, density: null,
    comment: (m.description || '') + ' (demo, tabulated from built-in)',
    color: m.color,
    group: m.group,
  };
}

const catalogs = {};
for (const p of PAIRS) {
  const materials = {};
  for (const id of p.members) materials[id] = sampleMaterial(id);
  catalogs[p.id] = { id: p.id, name: p.name, source: 'user', materials };
}

const banner = '// AUTO-GENERATED by tools/gen-demo-catalogs.mjs — do not edit by hand.\n'
  + '// Curated 2-material coating pairs for the web demo (tabulated n,k).\n';
fs.writeFileSync(out, banner + 'window.DEMO_CATALOGS = ' + JSON.stringify(catalogs) + ';\n', 'utf8');

const sizeKb = (fs.statSync(out).size / 1024).toFixed(1);
console.log(`[gen-demo-catalogs] wrote ${PAIRS.length} pairs → web/demo-catalogs.js (${sizeKb} kB)`);
