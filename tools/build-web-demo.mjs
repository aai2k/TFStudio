// Pipeline (wired into `npm run build:web`):
//   1. `node tools/build-renderer.mjs` has already produced build/app/ (the
//      browser-targeted esbuild bundle + vendored libs + index.html). This script
//      runs AFTER it and never re-bundles.
//   2. Copy build/app/ → web/dist/.
//   3. Copy the WASM TMM kernel next to index.html (the desktop loads it over IPC;
//      the web shim fetches it as a static file — see web/demo-shim.js).
//   4. Copy the demo scripts (demo-examples.js, demo-shim.js).
//   5. Rewrite web/dist/index.html: inject the two demo scripts BEFORE the renderer
//      module so window.DEMO_EXAMPLES + window.electronAPI exist at boot.
//
// Output: web/dist/ — a pure static site. 
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, '..');
const buildApp = path.join(root, 'build', 'app');
const webSrc = path.join(root, 'web');
const outDir = path.join(webSrc, 'dist');
const wasmSrc = path.join(root, 'src', 'wasm', 'tmm_kernel.wasm');

function clean(dir) {
  if (fs.existsSync(dir)) fs.rmSync(dir, { recursive: true, force: true });
  fs.mkdirSync(dir, { recursive: true });
}
function copyTree(from, to) {
  fs.mkdirSync(to, { recursive: true });
  for (const e of fs.readdirSync(from, { withFileTypes: true })) {
    const s = path.join(from, e.name), d = path.join(to, e.name);
    if (e.isDirectory()) copyTree(s, d);
    else if (e.isFile()) fs.copyFileSync(s, d);
  }
}

function main() {
  if (!fs.existsSync(buildApp)) {
    console.error('[build-web-demo] build/app/ missing — run `node tools/build-renderer.mjs` first.');
    process.exit(1);
  }

  console.log('[build-web-demo] cleaning web/dist …');
  clean(outDir);

  console.log('[build-web-demo] copying build/app → web/dist …');
  copyTree(buildApp, outDir);

  // Icons. The desktop build keeps icons in build/icons (sibling of build/app)
  // and the renderer references them as '../icons/…' relative to index.html. In
  // the flat web deploy index.html sits at the root, so we copy icons INTO
  // web/dist/icons and rewrite '../icons/' → 'icons/' (done in the HTML rewrite
  // step below + the bundle patch here) to keep the demo self-contained.
  const iconsSrc = path.join(root, 'build', 'icons');
  if (fs.existsSync(iconsSrc)) {
    copyTree(iconsSrc, path.join(outDir, 'icons'));
    const rendPath = path.join(outDir, 'renderer.js');
    let rend = fs.readFileSync(rendPath, 'utf8');
    rend = rend.split('../icons/').join('icons/');
    fs.writeFileSync(rendPath, rend, 'utf8');
    console.log('[build-web-demo] copied icons + rebased ../icons/ → icons/ in bundle');
  } else {
    console.warn('[build-web-demo] WARNING: build/icons not found — logo will 404.');
  }

  // WASM kernel (static fetch target for the web shim).
  if (fs.existsSync(wasmSrc)) {
    fs.copyFileSync(wasmSrc, path.join(outDir, 'tmm_kernel.wasm'));
    console.log('[build-web-demo] copied tmm_kernel.wasm');
  } else {
    console.warn('[build-web-demo] WARNING: tmm_kernel.wasm not found — demo falls back to the JS TMM path.');
  }

  // Demo scripts (demo-catalogs.js is generated by gen-demo-catalogs.mjs).
  // demo-shim.js carries a `X.Y.Z-web-demo` version placeholder reported by the
  // shim's getAppVersion(); stamp the live package.json version into it on copy
  // so the demo's About box never drifts from the desktop release.
  const appVersion = JSON.parse(fs.readFileSync(path.join(root, 'package.json'), 'utf8')).version;
  const demoScripts = ['demo-boot.js', 'demo-examples.js', 'demo-catalogs.js', 'demo-shim.js'];
  for (const f of demoScripts) {
    const src = path.join(webSrc, f);
    if (!fs.existsSync(src)) {
      console.error(`[build-web-demo] missing ${f} — run gen-demo-catalogs.mjs first.`);
      process.exit(1);
    }
    if (f === 'demo-shim.js') {
      let shim = fs.readFileSync(src, 'utf8');
      shim = shim.replace(/\d+\.\d+\.\d+-web-demo/g, `${appVersion}-web-demo`);
      fs.writeFileSync(path.join(outDir, f), shim, 'utf8');
    } else {
      fs.copyFileSync(src, path.join(outDir, f));
    }
  }
  console.log(`[build-web-demo] copied ${demoScripts.join(', ')} (version → ${appVersion}-web-demo)`);

  // ── Rewrite the <script> block into a deterministic, robust load order ────────
  // The DEMO must mount reliably on slow/mobile connections. Strategy:
  //   1. The SMALL critical scripts (demo-boot, React, ReactDOM, the demo globals)
  //      load BLOCKING and in dependency order — total ~200 kB, fast everywhere,
  //      and guaranteed to have executed before the renderer module runs.
  //   2. The renderer module mounts the UI using React alone.
  //   3. The HEAVY libs (Plotly ~3.5 MB, katex) load `async` AFTER the module, so
  //      they never block or gate the mount. Charts guard on `typeof Plotly`, and
  //      the demo opens with no chart visible, so Plotly is ready by the time one
  //      is opened. A slow/failed Plotly can no longer blank the page.
  // (Earlier `defer` on React stalled boot: deferred scripts run only AFTER the
  // parser finishes, so any parser hiccup left React undefined → nothing mounted.
  // Blocking-in-order is deterministic and avoids that class of failure.)
  const htmlPath = path.join(outDir, 'index.html');
  let html = fs.readFileSync(htmlPath, 'utf8');
  const marker = '<script type="module" src="renderer.js"></script>';
  if (!html.includes(marker)) {
    console.error('[build-web-demo] could not find renderer <script> marker in index.html');
    process.exit(1);
  }

  // Pull Plotly + katex out of their original (blocking, pre-renderer) position.
  html = html.replace('    <script src="vendor/plotly.min.js"></script>\n', '');
  html = html.replace('    <script src="vendor/katex.min.js"></script>\n', '');

  // Build stamp (shown by the splash) + boot diagnostics, before everything.
  const buildId = new Date().toISOString().replace(/\.\d+Z$/, 'Z');
  const reactMarker = '<script src="vendor/react.production.min.js"></script>';
  if (!html.includes(reactMarker)) {
    console.error('[build-web-demo] react <script> marker not found');
    process.exit(1);
  }
  html = html.replace(reactMarker,
    `<script>window.__TFS_BUILD__=${JSON.stringify(buildId)}</script>\n    `
    + '<script src="demo-boot.js"></script>\n    ' + reactMarker);

  // Demo globals (blocking, tiny) + renderer module + heavy async libs after it.
  const inject =
    '    <!-- Web demo: example designs + curated catalogs + browser electronAPI shim -->\n'
    + '    <script src="demo-examples.js"></script>\n'
    + '    <script src="demo-catalogs.js"></script>\n'
    + '    <script src="demo-shim.js"></script>\n'
    + '    ' + marker + '\n'
    + '    <!-- Heavy libs: load async AFTER the renderer so they never block the mount -->\n'
    + '    <script async src="vendor/plotly.min.js"></script>\n'
    + '    <script async src="vendor/katex.min.js"></script>';
  html = html.replace(marker, inject);

  // Rebase any '../icons/' in the HTML and add a favicon (the desktop index.html
  // has no <link rel=icon>; a browser tab wants one).
  html = html.split('../icons/').join('icons/');
  if (!/rel="icon"/.test(html)) {
    html = html.replace('</title>', '</title>\n    <link rel="icon" type="image/png" href="icons/tfstudio-purple2.png">');
  }
  // The CSP must allow same-origin fetch of the .wasm and its instantiation.
  // The renderer template already grants 'unsafe-eval' (covers WebAssembly) and
  // default-src 'self' (covers same-origin fetch), so no CSP edit is needed.
  fs.writeFileSync(htmlPath, html, 'utf8');
  console.log('[build-web-demo] injected demo scripts into index.html');

  // Optional mirror into a deploy dir (e.g. the Cloudflare Pages website repo).
  // Set TFS_WEB_DEPLOY_DIR to the target /demo folder; it is wiped + refilled.
  const deployDir = process.env.TFS_WEB_DEPLOY_DIR;
  if (deployDir) {
    console.log(`[build-web-demo] mirroring → ${deployDir} …`);
    clean(deployDir);
    copyTree(outDir, deployDir);
    console.log('[build-web-demo] mirror done');
  }

  console.log('[build-web-demo] done → web/dist/  (static — deploy as-is)');
}

main();
